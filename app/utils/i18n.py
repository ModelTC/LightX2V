def t(key: str, lang: str = None) -> str:
    """获取翻译文本"""
    if lang is None:
        lang = DEFAULT_LANG

    if lang not in TRANSLATIONS:
        lang = "zh"

    return TRANSLATIONS[lang].get(key, key)


def set_language(lang: str):
    """设置语言"""
    global DEFAULT_LANG
    if lang in TRANSLATIONS:
        DEFAULT_LANG = lang
        os.environ["GRADIO_LANG"] = lang
